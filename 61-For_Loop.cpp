// Print Number From 1 to N using For Loop
#include <iostream>
using namespace std;
int main(){
    int n;
    cin >> n;
    //In for Loop You Need to add Starting Value after that add Condition after semi color After that Increment or Decrement Value
    // For Loop Prevent Us fromt Loop hell (Where Loop is Run Infinity)
    for(int i = 1; i <= n;i++){
        cout << i << " ";
    }
}
